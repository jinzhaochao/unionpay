package com.unionpay.voice.model;

/**
 * 页面留言评论信息实体类
 * @author lishuang
 * @date 2019-05-09
 */
public class CommentModel {
    private String unid;
    private String infoUnid;
    private String showName;
    private String commentContent;
    private String createTime;
    private Integer incognito;

    public String getUnid() {
        return unid;
    }

    public void setUnid(String unid) {
        this.unid = unid;
    }

    public String getInfoUnid() {
        return infoUnid;
    }

    public void setInfoUnid(String infoUnid) {
        this.infoUnid = infoUnid;
    }

    public String getShowName() {
        return showName;
    }

    public void setShowName(String showName) {
        this.showName = showName;
    }

    public String getCommentContent() {
        return commentContent;
    }

    public void setCommentContent(String commentContent) {
        this.commentContent = commentContent;
    }

    public String getCreateTime() {
        return createTime;
    }

    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }

    public Integer getIncognito() {
        return incognito;
    }

    public void setIncognito(Integer incognito) {
        this.incognito = incognito;
    }

    @Override
    public String toString() {
        return "CommentModel{" +
                "unid=" + unid +
                ", infoUnid=" + infoUnid +
                ", showName=" + showName +
                ", commentContent=" + commentContent +
                ", createTime=" + createTime +
                ", incognito=" + incognito +
                '}';
    }
}
