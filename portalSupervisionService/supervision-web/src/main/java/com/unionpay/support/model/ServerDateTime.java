package com.unionpay.support.model;

import java.math.BigInteger;

/**
 * @Author: jinzhao
 * @Date: 2019/11/04 16:18
 * @Description:  根据地点和日期返回可展示的时间段实体类
 */
public class ServerDateTime {
    private Integer count;
    private BigInteger countNum;
    private String serverTime;
    private String myPlace;
    private String dateTime;

    public ServerDateTime() {
    }

    public ServerDateTime(Integer count, BigInteger countNum, String serverTime, String myPlace, String dateTime) {
        this.count = count;
        this.countNum = countNum;
        this.serverTime = serverTime;
        this.myPlace = myPlace;
        this.dateTime = dateTime;
    }

    public Integer getCount() {
        return count;
    }

    public void setCount(Integer count) {
        this.count = count;
    }

    public String getServerTime() {
        return serverTime;
    }

    public void setServerTime(String serverTime) {
        this.serverTime = serverTime;
    }

    public BigInteger getCountNum() {
        return countNum;
    }

    public void setCountNum(BigInteger countNum) {
        this.countNum = countNum;
    }

    public String getMyPlace() {
        return myPlace;
    }

    public void setMyPlace(String myPlace) {
        this.myPlace = myPlace;
    }

    public String getDateTime() {
        return dateTime;
    }

    public void setDateTime(String dateTime) {
        this.dateTime = dateTime;
    }

    @Override
    public String toString() {
        return "ServerDateTime{" +
                "count=" + count +
                ", countNum=" + countNum +
                ", serverTime='" + serverTime + '\'' +
                ", myPlace='" + myPlace + '\'' +
                ", dateTime='" + dateTime + '\'' +
                '}';
    }
}
