package com.unionpay.services.model;

import java.io.Serializable;

/**
 * @Author: 翟俊鹏
 * @Date: 2019/3/22/022 18:24
 * @Description:
 */
public class FrontPageRequest implements Serializable {

    private Integer page;

    private Integer size;

    private Integer orgId;

    private String keyword;

    private String text;

    private Integer onlineProcessing;

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public Integer getPage() {
        return page;
    }

    public void setPage(Integer page) {
        this.page = page;
    }

    public Integer getSize() {
        return size;
    }

    public void setSize(Integer size) {
        this.size = size;
    }

    public Integer getOrgId() {
        return orgId;
    }

    public void setOrgId(Integer orgId) {
        this.orgId = orgId;
    }

    public String getKeyword() {
        return keyword;
    }

    public void setKeyword(String keyword) {
        this.keyword = keyword;
    }

    public Integer getOnlineProcessing() {
        return onlineProcessing;
    }

    public void setOnlineProcessing(Integer onlineProcessing) {
        this.onlineProcessing = onlineProcessing;
    }
}
