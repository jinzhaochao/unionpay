package com.unionpay.sms.model;

/**
 * 发起短信审批接口参数-接收人
 *
 * @author lishuang
 * @date 2019-01-07
 */
public class Receiver {
    private String unid;
    private String receiver_id;
    private String receiver_name;
    private String receiver_tel;
    private Integer org_id;
    private String org_name;
    private String org_level;
    private String org_remark;
    private String type;

    public Receiver() {
    }

    public Receiver(String unid, String receiver_id, String receiver_name, String receiver_tel, Integer org_id, String org_name, String org_level, String org_remark, String type) {
        this.unid = unid;
        this.receiver_id = receiver_id;
        this.receiver_name = receiver_name;
        this.receiver_tel = receiver_tel;
        this.org_id = org_id;
        this.org_name = org_name;
        this.org_level = org_level;
        this.org_remark = org_remark;
        this.type = type;
    }

    public String getUnid() {
        return unid;
    }

    public void setUnid(String unid) {
        this.unid = unid;
    }

    public String getReceiver_id() {
        return receiver_id;
    }

    public void setReceiver_id(String receiver_id) {
        this.receiver_id = receiver_id;
    }

    public String getReceiver_name() {
        return receiver_name;
    }

    public void setReceiver_name(String receiver_name) {
        this.receiver_name = receiver_name;
    }

    public String getReceiver_tel() {
        return receiver_tel;
    }

    public void setReceiver_tel(String receiver_tel) {
        this.receiver_tel = receiver_tel;
    }

    public Integer getOrg_id() {
        return org_id;
    }

    public void setOrg_id(Integer org_id) {
        this.org_id = org_id;
    }

    public String getOrg_name() {
        return org_name;
    }

    public void setOrg_name(String org_name) {
        this.org_name = org_name;
    }

    public String getOrg_level() {
        return org_level;
    }

    public void setOrg_level(String org_level) {
        this.org_level = org_level;
    }

    public String getOrg_remark() {
        return org_remark;
    }

    public void setOrg_remark(String org_remark) {
        this.org_remark = org_remark;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public String toString() {
        return "Receiver{" +
                "unid='" + unid  +
                ", receiver_id='" + receiver_id +
                ", receiver_name='" + receiver_name +
                ", receiver_tel='" + receiver_tel +
                ", org_id=" + org_id +
                ", org_name='" + org_name +
                ", org_level='" + org_level +
                ", org_remark='" + org_remark +
                ", type='" + type +
                '}';
    }
}
