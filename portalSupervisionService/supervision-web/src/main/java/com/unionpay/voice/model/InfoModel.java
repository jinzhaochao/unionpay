package com.unionpay.voice.model;

/**
 * 详情页(前端)基本信息实体类
 * @author lishuang
 * @date 2019-05-10
 */
public class InfoModel {
    private String unid;
    private String title;
    private String callTime;
    private String transferTime;
    private String caseTag;
    private String caseType;
    private String infoContent;
    private String handleOrgName;
    private String handleTime;
    private String handleContent;

    public String getUnid() {
        return unid;
    }

    public void setUnid(String unid) {
        this.unid = unid;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getCallTime() {
        return callTime;
    }

    public void setCallTime(String callTime) {
        this.callTime = callTime;
    }

    public String getTransferTime() {
        return transferTime;
    }

    public void setTransferTime(String transferTime) {
        this.transferTime = transferTime;
    }

    public String getCaseTag() {
        return caseTag;
    }

    public void setCaseTag(String caseTag) {
        this.caseTag = caseTag;
    }

    public String getCaseType() {
        return caseType;
    }

    public void setCaseType(String caseType) {
        this.caseType = caseType;
    }

    public String getInfoContent() {
        return infoContent;
    }

    public void setInfoContent(String infoContent) {
        this.infoContent = infoContent;
    }

    public String getHandleOrgName() {
        return handleOrgName;
    }

    public void setHandleOrgName(String handleOrgName) {
        this.handleOrgName = handleOrgName;
    }

    public String getHandleTime() {
        return handleTime;
    }

    public void setHandleTime(String handleTime) {
        this.handleTime = handleTime;
    }

    public String getHandleContent() {
        return handleContent;
    }

    public void setHandleContent(String handleContent) {
        this.handleContent = handleContent;
    }

    @Override
    public String toString() {
        return "InfoModel{" +
                "unid=" + unid +
                ", title=" + title +
                ", callTime=" + callTime +
                ", transferTime=" + transferTime +
                ", caseTag=" + caseTag +
                ", caseType=" + caseType +
                ", infoContent=" + infoContent +
                ", handleOrgName=" + handleOrgName +
                ", handleTime=" + handleTime +
                ", handleContent=" + handleContent +
                '}';
    }
}
